    <h2>1. Introduction (Important!)</h2>
    <p>Congratulations! You've officially been recruited as an elite interstellar combat pilot by the highly esteemed Go Grob Enterprises. But this isn't just another day at the space-office—today you have the special honor (and slightly stressful responsibility) of helping the galaxy's most lovable yet surprisingly lazy alien, Grob, finally reach Earth for his desperately-needed vacation.</p>
    
    <p>Grob has been dreaming of soaking up Earth's peculiar charm ever since binge-watching countless human sitcoms and documentaries about beaches, amusement parks, and something mysterious called "pizza". Unfortunately, Grob's route to relaxation is anything but relaxing. Due to a frankly baffling oversight by Grob Enterprises' galactic navigation department, the path to Earth is densely populated with endless waves of mysterious rectangular space debris, randomly floating around the cosmos for reasons not even our brightest minds understand. Why rectangles, you ask? Absolutely no clue. Just consider them your mortal nemeses now and dodge accordingly.</p>

    <p>But fear not, brave pilot! To help you survive this perilous journey, we've thoughtfully scattered glowing orbs throughout the cosmos. Touch one of these inexplicably magical objects, and you'll instantly activate the legendary Grobster-5000's powerful (albeit temporary) hyperspeed mode, allowing you to blast straight through those infuriating rectangles in a dazzlingly explosive spectacle.</p>
    
    <p>Speaking of your spaceship—the Grobster-5000—is no ordinary interstellar vessel. It's an iconic marvel of futuristic technology and retro-chic aesthetics, equipped specifically to handle the inexplicable gravitational anomalies you'll constantly encounter. Engineered originally as a luxury cruiser for wealthy aliens seeking "adventure" without actual danger, it was hastily repurposed for Grob's ambitious vacation plans following a series of minor bureaucratic mishaps and minor existential crises.</p>

    <p>Admittedly, the Grobster-5000's safety record might raise a few eyebrows, but what it lacks in reassuring statistics, it more than compensates for in sheer style, personality, and undeniable charm. Plus, Grob personally promises it'll "probably be fine," and who are we to question such profound optimism?</p>

    <p>So buckle up, pilot, keep your clicking finger limber, and prepare to embark on the greatest (and possibly most unnecessarily dangerous) vacation escort mission ever attempted. Grob's ultimate Earth vacation—and potentially galactic diplomatic relations—depends entirely on your ability to navigate through space rectangles, grab glowing orbs, and maintain your sanity amidst endless hyperspeed-induced chaos.</p>

    <p>Good luck! Grob is already packing his Hawaiian shirt and novelty sunglasses. Try not to crash—you wouldn't want to disappoint him, would you?</p>

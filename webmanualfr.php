<div class="section">
    <h2>5. Dépannage / FAQ</h2>
    <ul>
        <li class="faq-question">
            <strong>Q :</strong> Mon vaisseau n’arrête pas de tomber. Que faire ?<br>
            <strong>R :</strong> Cliquez plus souvent. Votre vaisseau réagit à vos clics par une ascension tactique (ce qui veut dire “il monte un peu”, mais en plus chic). Si vous oubliez de cliquer, la gravité (oui, même dans l’espace, apparemment) vous attirera vers le bas sans pitié. Si cliquer vous semble trop dur, envisagez le jardinage spatial.
        </li>

        <li class="faq-question">
            <strong>Q :</strong> Puis-je arrêter les obstacles ?<br>
            <strong>R :</strong> Non. Les obstacles sont des rectangles cosmiques générés procéduralement dans le seul but de vous gâcher la journée. C’est l’univers qui vous dit “eh oh, pas si vite !”. Si vous les détestez vraiment, activez l’hyperspeed et défoncez-les… mais les arrêter ? Oubliez.
        </li>

        <li class="faq-question">
            <strong>Q :</strong> Y a-t-il un mode multijoueur ?<br>
            <strong>R :</strong> Techniquement, oui—si quelqu’un se tient derrière vous en hurlant “Tu as raté l’orbe !” ou “Pourquoi t’es aussi nul ?”, alors félicitations : vous jouez en coopératif. Le vrai multijoueur est prévu juste après l’effondrement du soleil.
        </li>

        <li class="faq-question">
            <strong>Q :</strong> Puis-je améliorer mon vaisseau ?<br>
            <strong>R :</strong> Absolument pas. Le Grobster-5000 représente déjà le summum de l’ingénierie rétro intergalactique. Tenter de l’améliorer risquerait de compromettre son système anti-gravité légalement discutable… voire de le rendre conscient. Vous ne voulez pas vivre ça.
        </li>

        <li class="faq-question">
            <strong>Q :</strong> Quelle est l’histoire derrière Go Grob ?<br>
            <strong>R :</strong> Cette information est classée top secret. Tout ce qu’on peut vous dire, c’est que Grob est un alien qui a désespérément besoin de vacances sur Terre, et que vous êtes son seul espoir. Le reste est enfermé dans un coffre-fort sous un cratère lunaire. N’insistez pas… on nous observe.
        </li>
    </ul>

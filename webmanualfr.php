    <h2>1. Introduction (Important !)</h2>
    <p>Félicitations ! Vous avez officiellement été recruté en tant que pilote de combat interstellaire d'élite par la très respectée Go Grob Enterprises. Mais ce n’est pas un jour ordinaire au bureau spatial : aujourd’hui, vous avez l’honneur (et la responsabilité légèrement angoissante) d’escorter l’extraterrestre le plus attachant – et étonnamment paresseux – de la galaxie, Grob, jusqu’à la Terre pour des vacances désespérément attendues.</p>

    <p>Grob rêve de découvrir les charmes étranges de la Terre depuis qu’il a binge-watché d’innombrables sitcoms humaines et documentaires sur les plages, les parcs d’attractions, et une chose mystérieuse appelée “pizza”. Malheureusement, le chemin vers la détente est tout sauf relaxant. À cause d’un oubli franchement déroutant du département de navigation galactique de Go Grob Enterprises, la route vers la Terre est envahie par des vagues sans fin de mystérieux débris spatiaux rectangulaires flottant au hasard. Pourquoi des rectangles ? Aucune idée. Considérez-les comme vos ennemis jurés. Esquivez-les en conséquence.</p>

    <p>Mais n’ayez crainte, courageux pilote ! Pour vous aider à survivre à ce périple périlleux, nous avons éparpillé dans le cosmos des orbes brillants. Touchez l’un de ces objets magiques (et inexplicables), et vous activerez instantanément le légendaire mode hyperspeed du Grobster-5000, vous permettant de traverser les rectangles énervants dans un spectacle explosif éblouissant.</p>

    <p>En parlant de votre vaisseau – le Grobster-5000 – ce n’est pas un simple vaisseau spatial. C’est une merveille emblématique de technologie futuriste et d’esthétique rétro-chic, spécialement conçu pour gérer les anomalies gravitationnelles inexplicables que vous rencontrerez constamment. Conçu à l’origine comme croiseur de luxe pour aliens fortunés amateurs d’aventures sans danger réel, il a été rapidement réaffecté au projet vacances de Grob après quelques malentendus bureaucratiques et légères crises existentielles.</p>

    <p>Certes, les statistiques de sécurité du Grobster-5000 peuvent susciter quelques interrogations, mais ce qu’il manque en chiffres rassurants, il le compense largement par son style, sa personnalité et son charme indéniable. D’ailleurs, Grob promet personnellement que “ça devrait aller”. Et qui sommes-nous pour remettre en question un tel optimisme ?</p>

    <p>Alors accrochez-vous, pilote, échauffez votre doigt de clic et préparez-vous à embarquer dans la mission d’escorte de vacances la plus spectaculaire (et inutilement dangereuse) jamais tentée. Les vacances ultimes de Grob – et peut-être même la paix galactique – dépendent entièrement de votre capacité à esquiver des rectangles spatiaux, attraper des orbes brillants, et garder votre sang-froid au milieu du chaos hyperspeed.</p>

    <p>Bonne chance ! Grob a déjà préparé sa chemise hawaïenne et ses lunettes de soleil rigolotes. Essayez de ne pas vous écraser – il serait vraiment déçu.</p>
